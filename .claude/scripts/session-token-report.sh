# Cr√©er r√©pertoire rapports si n√©cessaire
mkdir -p "$REPORTS_DIR"

# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# FONCTIONS
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

print_separator() {
    echo -e "${CYAN}‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ${NC}"
}

get_session_data() {
    # R√©cup√®re donn√©es session courante via ccusage
    npx --yes ccusage@latest session --json 2>/dev/null | head -1 || echo "{}"
}

get_daily_data() {
    # R√©cup√®re donn√©es journali√®res
    npx --yes ccusage@latest daily --json 2>/dev/null | head -1 || echo "{}"
}

generate_report() {
    local session_data="$1"
    local daily_data="$2"

    echo -e "\n${CYAN}${BOLD}‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê${NC}"
    echo -e "${CYAN}${BOLD}  üìä RAPPORT FIN DE SESSION - $(date '+%Y-%m-%d %H:%M')${NC}"
    echo -e "${CYAN}${BOLD}‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê${NC}\n"

    # Extract session stats
    local session_tokens=$(echo "$session_data" | grep -o '"totalTokens":[0-9]*' | head -1 | cut -d':' -f2)
    local session_cost=$(echo "$session_data" | grep -o '"totalCost":[0-9.]*' | head -1 | cut -d':' -f2)

    # Extract daily stats
    local daily_tokens=$(echo "$daily_data" | grep -o '"totalTokens":[0-9]*' | head -1 | cut -d':' -f2)
    local daily_cost=$(echo "$daily_data" | grep -o '"totalCost":[0-9.]*' | head -1 | cut -d':' -f2)

    # Defaults
    session_tokens=${session_tokens:-0}
    session_cost=${session_cost:-0.0000}
    daily_tokens=${daily_tokens:-0}
    daily_cost=${daily_cost:-0.0000}

    echo -e "${WHITE}Session Courante:${NC}"
    echo -e "  Tokens:  $(printf "%'d" $session_tokens 2>/dev/null || echo $session_tokens)"
    echo -e "  Co√ªt:    \$${session_cost}"

    print_separator

    echo -e "${WHITE}Aujourd'hui (Total):${NC}"
    echo -e "  Tokens:  $(printf "%'d" $daily_tokens 2>/dev/null || echo $daily_tokens)"
    echo -e "  Co√ªt:    \$${daily_cost}"

    # Budget status
    local daily_budget=5.00
    if (( $(echo "$daily_cost >= $daily_budget * 0.9" | bc -l 2>/dev/null || echo 0) )); then
        echo -e "  Status:  ${YELLOW}‚ö† Budget journalier proche limite${NC}"
    else
        echo -e "  Status:  ${GREEN}‚úÖ Budget OK${NC}"
    fi

    print_separator

    # Recommendations
    echo -e "\n${BLUE}${BOLD}üí° RECOMMANDATIONS:${NC}"

    if (( session_tokens > 10000 )); then
        echo -e "  ${YELLOW}‚ñ∏${NC} Session longue d√©tect√©e (${session_tokens} tokens)"
        echo -e "    ${WHITE}‚Üí${NC} Diviser en sessions plus courtes (<10k tokens)"
    fi

    if (( $(echo "$session_cost > 1.0" | bc -l 2>/dev/null || echo 0) )); then
        echo -e "  ${YELLOW}‚ñ∏${NC} Co√ªt session √©lev√© (\$${session_cost})"
        echo -e "    ${WHITE}‚Üí${NC} Optimiser prompts et utiliser cache"
    fi

    echo -e "\n${CYAN}${BOLD}üéØ COMMANDES UTILES:${NC}"
    echo -e "  ${WHITE}claude-monitor${NC}              # Dashboard temps r√©el"
    echo -e "  ${WHITE}npx ccusage daily${NC}           # Rapport d√©taill√©"
    echo -e "  ${WHITE}/token-stats --quick${NC}        # R√©sum√© rapide"

    echo -e "\n${CYAN}${BOLD}‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê${NC}\n"
}

log_session() {
    local session_data="$1"
    local timestamp=$(date -u +"%Y-%m-%dT%H:%M:%SZ")

    # Append to session log
    echo "{\"timestamp\":\"$timestamp\",\"data\":$session_data}" >> "$SESSION_LOG"
}

# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
# MAIN
# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

main() {
    # R√©cup√©ration donn√©es
    local session_data=$(get_session_data)
    local daily_data=$(get_daily_data)

    # G√©n√©ration rapport
    generate_report "$session_data" "$daily_data"

    # Logging session
    log_session "$session_data"

    # Export rapport journalier JSON
    local daily_report="${REPORTS_DIR}/daily-$(date +%Y-%m-%d).json"
    echo "$daily_data" > "$daily_report"
}

# Ex√©cution silencieuse (pas d'erreur si outils manquants)
main 2>/dev/null || {
    echo -e "${YELLOW}‚ö† Monitoring tokens non disponible (ccusage manquant?)${NC}" >&2
    echo -e "${CYAN}Installation: npx ccusage@latest${NC}" >&2
    exit 0
}
