## Les erreurs courantes

**Choisir trop petit pour la surface** : une lampe dont le diffuseur fait 20 cm de diamètre sur une grande table de salon disparaît visuellement. Règle générale — le diffuseur doit occuper au moins 1/3 de la largeur de la surface sur laquelle elle repose.

**Confondre hauteur de lampe et hauteur de lumière** : ce qui compte, c''est à quelle hauteur le bas du diffuseur se trouve — c''est là que part la lumière utile. Deux lampes de 60 cm de hauteur totale peuvent avoir des diffuseurs à des hauteurs très différentes selon la longueur du pied.

**Oublier l''ampoule** : les lampes design sont conçues pour certains types d''ampoules. Une ampoule LED trop puissante dans un abat-jour métal non diffusant devient une lampe d''interrogatoire. Vérifiez les préconisations et préférez les ampoules à spectre chaud (2700 K) pour tous les usages résidentiels.

**Multiplier les matières métalliques différentes dans une même pièce** : si vous choisissez une lampe chromée, assurez-vous que les autres accessoires métalliques de la pièce ne sont pas tous en laiton doré — ou assumez le mix (voir l''article sur les compositions de miroirs).

