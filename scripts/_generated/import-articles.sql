**La relation avec la lumière** : placez vos miroirs face à une source de lumière naturelle — fenêtre en vis-à-vis ou fenêtre latérale — pour que la réflexion travaille vraiment. Un miroir dos à la lumière ne fait que refléter un mur sombre.

